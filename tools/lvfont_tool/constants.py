def get_block(codepoint):
    for start, end, en, cn in UNICODE_BLOCKS:
        if start <= codepoint <= end:
            return (en, cn)
    lo = codepoint & ~0xFF
    hi = lo + 0xFF
    return (f"U+{lo:04X}–U+{hi:04X}", f"U+{lo:04X}–U+{hi:04X}")


